// Discover multi-endpoint routes for every catalog API (docs, OpenAPI, heuristics).
// run: node scripts/expand-endpoints.mjs [--limit 100] [--concurrency 20]
// then: node scripts/assemble-seed.mjs && npm run seed:sql

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { expandEndpoints } from './lib/endpoint-expander.mjs'
import { importedSpecs } from '../src/data/seed-imported.ts'

const LIMIT = Number(process.argv.find((a, i) => process.argv[i - 1] === '--limit') || '99999')
const CONC = Number(process.argv.find((a, i) => process.argv[i - 1] === '--concurrency') || '20')
const CACHE_DIR = fileURLToPath(new URL('./.cache/endpoints/', import.meta.url))
const OUT = fileURLToPath(new URL('./lib/generated-endpoints.mjs', import.meta.url))

mkdirSync(CACHE_DIR, { recursive: true })

const specs = importedSpecs.slice(0, LIMIT)

console.log(`expanding endpoints for ${specs.length} APIs (${CONC} concurrent)`)

/** @type {Record<string, import('./lib/endpoint-expander.mjs').EndpointRow[]>} */
const generated = {}
let done = 0
let expanded = 0

async function work(spec) {
  const cacheFile = `${CACHE_DIR}${spec.slug}.json`
  if (existsSync(cacheFile)) {
    generated[spec.slug] = JSON.parse(readFileSync(cacheFile, 'utf8'))
    return
  }
  const endpoints = await expandEndpoints(spec)
  if (endpoints.length > 1) expanded++
  generated[spec.slug] = endpoints
  writeFileSync(cacheFile, JSON.stringify(endpoints, null, 2) + '\n')
  process.stdout.write(`  ✓ ${spec.slug} (${endpoints.length} routes)\n`)
}

const queue = [...specs]
await Promise.all(Array.from({ length: CONC }, async () => {
  for (let s; (s = queue.shift()); ) {
    await work(s)
    done++
    if (done % 50 === 0) process.stdout.write(`  … ${done}/${specs.length}\n`)
  }
}))

const lines = [
  '// AUTO-GENERATED by scripts/expand-endpoints.mjs — do not hand-edit.',
  `// ${Object.keys(generated).length} APIs · ${expanded} expanded beyond 1 route`,
  '',
  '/** @type {Record<string, Array<{ method?: string, path: string, description: string, monitored?: boolean }>>} */',
  `export const GENERATED_ENDPOINTS = ${JSON.stringify(generated, null, 2)}`,
  '',
]

writeFileSync(OUT, lines.join('\n'))
console.log(`\nwrote ${OUT}`)
console.log(`expanded: ${expanded}/${specs.length} with 2+ routes`)
